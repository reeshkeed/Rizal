        <p>
          An Act to Include in the Curricula of All Public and Private Schools, Colleges and Universities courses on the
          Life Works and Writings of JOSE RIZAL, particularly his novels NOLI ME TANGERE and EL FILIBUSTERISMO,
          Authorizing the Printing and Distribution Thereof, and for Other Purposes.
        </p>
        <p>
          Whereas, today, more than other period of our history, there is a need for a re-dedication to the ideals of freedom
          and nationalism for which our heroes lived and died.
        </p>
        <p>
          Whereas, it is meet that in honoring them, particularly the national hero and patriot, Jose Rizal,
          we remember with special fondness and devotion their lives and works that have shaped the national character;
        </p>
        <p>
          Whereas, the life, works and writings of Jose Rizal particularly his novels
          Noli Me Tangere and El Filibusterismo, are a constant and inspiring source of patriotism with which the minds of the youth,
          especially during their formative and decisive years in school, should be suffused.
        </p>
        <p>
          Whereas, all educational institutions are under the supervision of, and subject to regulation by the State,
          and all schools are enjoined to develop moral character, personal discipline, civic conscience, and to teach the duties of citizenship;
          Now therefore,
        </p>
        <p>
          Be it enacted by the Senate and House of Representatives of the Philippines in Congress assembled.
        </p>
      </div>

      <div class="wrapper">
        <h6>
          SECTION 1
        </h6>
        <p>
          Courses on the life, works and writings of Jose Rizal, particularly his novels Noli Me Tangere and El Filibusterismo,
          shall be included in the curricula of all schools, colleges and universities, public or private; Provided,
          That in the collegiate courses, the original or unexpurgated editions of the Noli Me Tangere and El Filibusterismo or
          their English translations shall be used as basic texts.
        </p>
        <p>
          The Board of National Education is hereby authorized and directed to adopt forthwith measures to implement and
          carry out the provisions of this Section, including the writing and printing of appropriate primers, readers and textbooks.
          The Board shall, within sixty (60) days from the effectivity of this Act promulgate rules and regulations, including those of a
          disciplinary nature, to carry out and enforce the regulations of this Act. The Board shall promulgate rules and regulations
          providing for the exemption of students for reason of religious belief stated in a sworn written statement, from the requirement
          of the provision contained in the second part of the first paragraph of this section; but not from taking the course provided for
          in the first part of said paragraph. Said rules and regulations shall take effect thirty (30) days after their publication in the
          Official Gazette.
        </p>
      </div>

      <div class="wrapper">
        <h6>
          SECTION 2
        </h6>
        <p>
          It shall be obligatory on all schools, colleges and universities to keep in their libraries an adequate number of copies of
          the original and expurgated editions of the Noli Me Tangere and El Filibusterismo, as well as Rizalâ€™s other works and biography.
          The said unexpurgated editions of the Noli Me Tangere and El Filibusterismo or their translations in English as well as other
          writings of Rizal shall be included in the list of approved books for required reading in all public or private schools, colleges
          and universities.
        </p>

        <p>
          The Board of National Education shall determine the adequacy of the number of books,
          depending upon the enrollment of the school, college or university.
        </p>
      </div>

      <div class="wrapper">
        <h6>
          SECTION 3
        </h6>
        <p>
          The Board of National education shall cause the translation of the Noli Me Tangere and El Filibusterismo, as well as other
          writings of Jose Rizal into English, Tagalog and the principal Philippine dialects; cause them to be printed in cheap,
          popular editions; and cause them to be distributed, free of charge, to persons desiring to read them,
          through the Purok organizations and the Barrio Councils throughout the country.
        </p>
      </div>

