          TO CREATE A COMMITTEE TO TAKE CHARGE OF THE PROPER CELEBRATION OF RIZAL DAY IN EVERY MUNICIPALITY AND CHARTERED CITY,
          AND FOR OTHER PURPOSES.
        </p>
