        <h3>Memorandum Order No. 247</h3>
      </div>

      <div class="wrapper">
        <p>
          DIRECTING THE SECRETARY OF EDUCATION, CULTURE AND SPORTS AND THE CHAIRMAN OF THE COMMISSION ON HIGHER EDUCATION
          TO FULLY IMPLEMENT REPUBLIC ACT NO. 1425 ENTITLED "AN ACT TO INCLUDE IN THE CURRICULA OF ALL PUBLIC AND PRIVATE SCHOOLS,
          COLLEGES AND UNIVERSITIES, COURSES ON THE LIFE, WORKS AND WRITINGS OF JOSE RIZAL, PARTICULARLY HIS NOVELS, NOLI ME TANGERE
          AND EL FILIBUSTERISMO, AUTHORIZING THE PRINTING AND DISTRIBUTION THEREOF AND FOR OTHER PURPOSES"
        </p>
        <p>
          WHEREAS, Republic Act No. 1425 approved on June 12, 1956, directs all schools, colleges and universities,
          public and private, to include in their curricula, courses on the life, works and writings of Jose Rizal,
          particularly his novels Noli Me Tangere and El Filibusterismo which "are a constant and inspiring source of patriotism
          with which the minds of the youth, especially during their formative and decisive years in school should be suffused;"
        </p>
        <p>
          WHEREAS, according to Dr. Rizal, "the school is the book in which is written the future of the nation;"
        </p>
        <p>
          WHEREAS, in 1996, the Filipino people will commemorate the centennial of Rizalâ€™s martyrdom and,
          two years thereafter, the centennial of the Declaration of Philippine Independence; and
        </p>
        <p>
          WHEREAS, as we prepare to celebrate these watershed events in our history, it is necessary to rekindle in the heart of every Filipino,
          especially the youth, the same patriotic fervor that once galvanized our forebears to outstanding achievements so we can move forward
          together toward a greater destiny as we enter the 21st century.
        </p>
        <p>
          NOW, THEREFORE, I FIDEL V. RAMOS, President of the Republic of the Philippines, by virtue of the powers vested in me by law,
          hereby direct the Secretary of Education, Culture and Sports and the Chairman of the Commission on Higher Education to take
          steps to immediately and fully implement the letter, intent and spirit of Republic Act No. 1425 and to impose,
          should it be necessary, appropriate disciplinary action against the governing body and/or head of any public or private school,
          college or university found not complying with said law and the rules, regulations, orders and instructions issued pursuant there to.
        </p>
        <p>
          Within thirty (30) days from issuance hereof, the Secretary of Education, Culture and Sports and
          the Chairman of the Commission on Higher Education are hereby directed to jointly submit to the President of the Philippines a
          report on the steps they have taken to implement this Memorandum Order, and one (1) year thereafter, another report on the extent
          of compliance by both public and private schools in all levels with the provisions of R.A. No. 1425.
        </p>
        <p>
          This Memorandum Order takes effect immediately after its issuance.
        </p>
        <p>
          DONE in the City of Manila, this 26th day of December in the year of Our Lord, Nineteen Hundred and Ninety-Four.
        </p>
        <p>
          (SGD.) FIDEL V. RAMOS <br>
          President
