      <div class="wrapper">
        <h6>
          SECTION 1
        </h6>
        <p>
          The existing laws and regulations to the contrary notwithstanding, cockfighting,
          horse racing and jai-alai are hereby prohibited on the thirtieth day of December each year,
          the date of the martyrdom of our great hero, Jose Rizal.
        </p>
      </div>

      <div class="wrapper">
        <h6>
          SECTION 2
        </h6>
        <p>
          It shall be the official duty of the mayor of each municipality and chartered city to create a committee
          to take charge of the proper observance of Rizal Day Celebration of each year, in which he shall be the chairman,
          which shall be empowered to seek the assistance and co-operation of any department, bureau, office, agency or
          instrumentality of the Government, and the local civic and educational institutions. Among the ceremonies on Rizal Day
          shall be the raising of the Philippine flag at half mast in all vessels and public buildings.
        </p>
      </div>

      <div class="wrapper">
        <h6>
          SECTION 3
        </h6>
        <p>
          Any person who shall violate the provisions of this Act or permit or allow the violation thereof,
          shall be punished by a fine of not exceeding two hundred pesos or by imprisonment not exceeding six months, or both,
          at the discretion of the court. In case he is the mayor of a municipality or a chartered city he shall suffer an additional
          punishment of suspension from his office for a period of one month. In case of partnerships, corporations or associations,
          the criminal liability shall devolve upon the president, director, or any other official responsible for the violation thereof.
        </p>
