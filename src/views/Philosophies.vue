        <h6>
          Educational Philosophy
        </h6>
        <p>
          Rizal’s concept of the importance of education is clearly enunciated in his work entitled Instruction wherein he
          sought improvements in the schools and in the methods of teaching. He maintained that the backwardness of his country
          during the Spanish ear was not due to the Filipinos’ indifference, apathy or indolence as claimed by the rulers, but to the
          neglect of the Spanish authorities in the islands. For Rizal, the mission of education is to elevate the country to the highest
          seat of glory and to develop the people’s mentality. Since education is the foundation of society and a prerequisite for social progress,
          Rizal claimed that only through education could the country be saved from domination.
        </p>
        <p>
          Rizal’s philosophy of education, therefore, centers on the provision of proper motivation in order to bolster the great
          social forces that make education a success, to create in the youth an innate desire to cultivate his intelligence and give him life eternal.
        </p>
