          Social Philosophy
        </h6>
        <p>
          That body of knowledge relating to society including the wisdom which man's experience in society has taught him is social philosophy.
          The facts dealt with are principles involved in nation building and not individual social problems. The subject matter of this social
          philosophy covers the problems of the whole race, with every problem having a distinct solution to bolster the people’s social knowledge.
        </p>
        <p>
          Rizal’s social philosophy dealt with:
        </p>
        <ul>
          <li>Man in society</li>
          <li>Influential factors in human life</li>
          <li>Racial problems</li>
          <li>Social constant</li>
          <li>Social justice</li>
          <li>Social ideal</li>
          <li>Poverty and wealth</li>
          <li>Reforms</li>
          <li>Youth and greatness</li>
          <li>History and progress</li>
          <li>Future Philippines</li>
        </ul>
        <p>
