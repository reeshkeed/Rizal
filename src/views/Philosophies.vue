        <h6>
          Religious Philosophy
        </h6>
        <p>
          Rizal grew up nurtured by a closely-knit Catholic family, was educated in the foremost Catholic schools of the period in the elementary,
          secondary and college levels; logically, therefore, he should have been a propagator of strictly Catholic traditions. However,
          in later life, he developed a life philosophy of a different nature, a philosophy of a different Catholic practice intermingled
          with the use of Truth and Reason.
        </p>
        <p>Why the change?</p>
        <p>
          It could have been the result of contemporary contact, companionship, observation, research and the possession of an independent spirit.
          Being a critical observer, a profound thinker and a zealous reformer, Rizal did not agree with the prevailing Christian propagation
          of the Faith by fire and sword. This is shown in his Annotation of Morga’s Sucesos de las Islas Filipinas.
        </p>
        <p>
          Rizal did not believe in the Catholic dogma that salvation was only for Catholics and that outside Christianity, salvation was not
          possible even if Catholics composed only a small minority of the world’s religious groups. Nor did he believe in the Catholic observation
          of fasting as a sacrifice, nor in the sale of such religious items as the cross, medals, rosaries and the like in order to propagate
          the Faith and raise church funds. He also lambasted the superstitious beliefs propagated by the priests in the church and in the schools.
          All of these and a lot more are evidences of Rizal’s religious philosophy. 
        </p>
