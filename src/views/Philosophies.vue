        <h6>
          Political Philosophy
        </h6>
        <p>
          In Rizal’s political view, a conquered country like the Philippines should not be taken advantage of but rather should be developed,
          civilized, educated and trained in the science of self-government.
        </p>
        <p>
          He bitterly assailed and criticized in publications the apparent backwardness of the Spanish ruler’s method of governing the country
          which resulted in: 
        </p>
        <ul>
          <li>The bondage and slavery of the conquered</li>
          <li>The Spanish government’s requirement of forced labor and force military service upon the n natives</li>
          <li>The abuse of power by means of exploitation</li>
          <li>The government ruling that any complaint against the authorities was criminal</li>
          <li>And making the people ignorant, destitute and fanatic, thus discouraging the formation of a national sentiment</li>
        </ul>
        <p>
          Rizal’s guiding political philosophy proved to be the study and application of reforms, the extension of human rights,
          the training for self government and the arousing of spirit of discontent over oppression, brutality, inhumanity,
          sensitiveness and self love.
        </p>
