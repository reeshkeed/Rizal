        <h6>
          Ethical Philosophy
        </h6>
        <p>
          The study of human behavior as to whether it is good or bad or whether it is right or wrong is that science upon which Rizal’s
          ethical philosophy was based. The fact that the Philippines was under Spanish domination during Rizal’s time led him to subordinate
          his philosophy to moral problems. This trend was much more needed at that time because the Spaniards and the Filipinos
          had different and sometimes conflicting morals. The moral status of the Philippines during this period was one with
          a lack of freedom, one with predominance of foreign masters, one with an imposition of foreign religious worship, devotion,
          homage and racial habits. This led to moral confusion among the people, what with justice being stifled, limited or
          curtailed and the people not enjoying any individual rights.
        </p>
        <p>
          To bolster his ethical philosophy, Dr. Rizal had recognized not only the forces of good and evil, but also the tendencies towards
          good and evil. As a result, he made use of the practical method of appealing to the better nature of the conquerors and of offering
          useful methods of solving the moral problems of the conquered.
        </p>
        <p>
          To support his ethical philosophy in life, Rizal:
        </p>
        <ul>
          <li>Censured the friars for abusing the advantage of their position as spiritual leaders and the ignorance and fanaticism of the natives</li>
          <li>Counseled the Filipinos not to resent a defect attributed to them but to accept same as reasonable and just</li>
          <li>Advised the masses that the object of marriage was the happiness and love of the couple and not financial gain</li>
          <li>Censured the priests who preached greed and wrong morality</li>
          <li>Advised every one that love and respect for parents must be strictly observed</li>
        </ul>
