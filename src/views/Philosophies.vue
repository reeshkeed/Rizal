          the voluntary offer of man’s abilities and possibilities; the ability to think, aspire and strive to rise; and the proper use of hearth,
          brain and spirit-all of these combining to enhance the intricacies, beauty and values of human nature. All of the above
          served as Rizal’s guide in his continuous effort to make over his beloved Philippines.
