        </p>

        <p>
          At about this time two of his mother’s cousin frequented Calamba. Uncle Manuel Alberto, seeing Rizal frail in body, concerned himself with
          the physical development of his young nephew and taught the latter love for the open air and developed in him a great admiration for the
          beauty of nature, while Uncle Gregorio, a scholar, instilled into the mind of the boy love for education. He advised Rizal: "Work hard and
          perform every task very carefully; learn to be swift as well as thorough; be independent in thinking and make visual pictures of everything."
        </p>
      </div>

