        <h6>
          Early Schooling in Biñan
        </h6>
        <p>
          Jose had a very vivid imagination and a very keen sense of observation. At the age of seven he traveled with his father for
          the first time to Manila and thence to Antipolo to fulfill the promise of a pilgrimage made by his mother at the time of his birth.
          They embarked in a casco, a very ponderous vessel commonly used in the Philippines. It was the first trip on the lake
          that Jose could recollect. As darkness fell he spent the hours by the katig, admiring the grandeur of the water and the
          stillness of the night, although he was seized with a superstitious fear when he saw a water snake entwine itself around the
          bamboo beams of the katig. With what joy did he see the sun at the daybreak as its luminous rays shone upon the glistening surface
          of the wide lake, producing a brilliant effect! With what joy did he talk to his father, for he had not uttered a word during the night!
        </p>
        <p>
          When they proceeded to Antipolo, he experienced the sweetest emotions upon seeing the gay banks of the Pasig and the towns of Cainta
          and Taytay. In Antipolo he prayed, kneeling before the image of the Virgin of Peace and Good Voyage, of whom he would later sing
          in elegant verses. Then he saw Manila, the great metropolis , with its Chinese sores and European bazaars. And visited his elder
          sister, Saturnina, in Santa Ana, who was a boarding student in the Concordia College.
        </p>
        <p>
          When he was nine years old, his father sent him to Biñan to continue studying Latin, because his first teacher had died.
          His brother Paciano took him to Biñan one Sunday, and Jose bade his parents and sisters good-bye with tears in his eyes.
          Oh, how it saddened him to leave for the first time and live far from his home and his family! But he felt ashamed to cry and
          had to conceal his tears and sentiments. "O Shame," he explained, "how many beautiful and pathetic scenes the world
          would witness without thee!"
        </p>
        <p>
          They arrived at Biñan in the evening. His brother took him to the house of his aunt where he was to stay, and left him after
          introducing him to the teacher. At night, in company with his aunt’s grandson named Leandro, Jose took a walk around the town
          in the light of the moon. To him the town looked extensive and rich but sad and ugly.
        </p>
        <p>
          His teacher in Biñan was a severe disciplinarian. His name was Justiniano Aquino Cruz. "He was a tall man, lean and long-necked,
          with a sharp nose and a body slightly bent forward. He used to wear a sinamay shirt woven by the deft hands of Batangas women.
          He knew by memory the grammars of Nebrija and Gainza. To this add a severity which, in my judgement I have made of him,
          which is all I remember."
        </p>
        <p>
          The boy Jose distinguished himself in class, and succeeded in surpassing many of his older classmates. Some of these were
          so wicked that, even without reason, they accused him before the teacher, for which, in spite of his progress, he received
          many whippings and strokes from the ferule. Rare was the day when he was not stretched on the bench for a whipping or punished
          with five or six blows on the open palm. Jose’s reaction to all these punishments was one of intense resentment in order to
          learn and thus carry out his father’s will.
        </p>
        <p>
          Jose spent his leisure hours with Justiniano’s father-in-law, a master painter. From him he took his first two sons, two nephews,
          and a grandson. His way life was methodical and well regulated. He heard mass at four if there was one that early,
          or studied his lesson at that hour and went to mass afterwards. Returning home, he might look in the orchard for a mambolo fruit to eat,
          then he took his breakfast, consisting generally of a plate of rice and two dried sardines. 
        </p>
        <p>
          After that he would go to class, from which he was dismissed at ten, then home again. He ate with his aunt and then began at ten,
          then home again. He ate with his aunt and then began to study. At half past two he returned to class and left at five.
          He might play for a short time with some cousins before returning home. He studied his lessons, drew for a while, and then
          prayed and if there was a moon, his friends would invite him to play in the street in company with other boys.
        </p>
        <p>
          Whenever he remembered his town, he thought with tears in his eyes of his beloved father, his idolized mother,
          and his solicitous sisters. Ah, how sweet was his town even though not so opulent as Biñan! He grew sad and thoughtful.
        </p>
        <p>
          While he was studying in Biñan, he returned to his hometown now and then. How long the road seemed to him in going and
          how short in coming! When from afar he descried the roof of his house, secret joy filled his breast. How he looked for
          pretexts to remain longer at home! A day more seemed to him a day spent in heaven, and how he wept, though silently and secretly,
          when he saw the calesa that was flower that him Biñan! Then everything looked sad; a flower that he touched, a stone that
          attracted his attention he gathered, fearful that he might not see it again upon his return. It was a sad but delicate and
          quite pain that possessed him. 
