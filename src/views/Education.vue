        <h6>
          First Day in Biñan School
        </h6>
        <p>
          The next morning (Monday) Paciano brought his younger brother to the school of Maestro Justiniano Aquino Cruz.
          The school was in the house of the teacher, which was a small nipa hut about 30 meters from the home of Jose’s aunt.mPaciano
          knew the teacher quite well because he had been a pupil under him before. He introduced Jose to the teacher, after which he departed
          to return to Calamba. Immediately, Jose was assigned his seat in the class. The teacher asked him:
        </p>
        <p>"Do you know Spanish?"</p>
        <p>"A little, sir," replied the Calamba lad.</p>
        <p>"Do you know Latin?"</p>
        <p>"A little, sir."</p>
        <p>
          The boys in the class, especially Pedro, the teacher’s son laughed at Jose’s answers. The teacher sharply stopped all noises and
          begun the lessons of the day. Jose described his teacher in Biñan as follows: "He was tall, thin, long-necked, with sharp nose and a
          body slightly bent forward, and he used to wear a sinamay shirt, woven by the skilled hands of the women of Batangas. He knew by the
          heart the grammars by Nebrija and Gainza. Add to this severity that in my judgement was exaggerated and you have a picture,
          perhaps vague, that I have made of him, but I remember only this."
        </p>
