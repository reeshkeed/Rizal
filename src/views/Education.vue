        <h6>
          First School Brawl
        </h6>
        <p>
          In the afternoon of his first day in school, when the teacher was having his siesta, Jose met the bully, Pedro.
          He was angry at this bully for making fun of him during his conversation with the teacher in the morning. Jose challenged Pedro to a fight.
          The latter readily accepted, thinking that he could easily beat the Calamba boy who was smaller and younger. The two boys wrestled
          furiously in the classroom, much to the glee of their classmates. Jose, having learned the art of wrestling from his athletic Tio Manuel,
          defeated the bigger boy. For this feat, he became popular among his classmates. After the class in the afternoon,
          a classmate named Andres Salandanan challenged him to an arm-wrestling match. They went to a sidewalk of a house and
          wrestled with their arms. Jose, having the weaker arm, lost and nearly cracked his head on the sidewalk.
          In succeeding days he had other fights with the boys of Biñan. He was not quarrelsome by nature, but he never ran away from a fight.
        </p>
