        <h6>
          Early Education in Calamba and Biñan
        </h6>
        <p>
          Rizal had his early education in Calamba and Biñan. It was a typical schooling that a son of an ilustrado family received during his time,
          characterized by the four R’s- reading, writing, arithmetic, and religion. Instruction was rigid and strict. Knowledge was forced into the
          minds of the pupils by means of the tedious memory method aided by the teacher’s whip. Despite the defects of the
          Spanish system of elementary education, Rizal was able to acquire the necessary instruction preparatory for college work in Manila.
          It may be said that Rizal, who was born a physical weakling, rose to become an intellectual giant not because of, but rather in spite of,
          the outmoded and backward system of instruction obtaining in the Philippines during the last decades of Spanish regime.
        </p>
