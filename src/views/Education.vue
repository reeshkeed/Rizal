        <h6>
          Jose Goes to Biñan
        </h6>
        <p>
          One Sunday afternoon in June , 1869, Jose, after kissing the hands of his parents and a tearful parting from his sister,
          left Calamba for Biñan. He was accompanied by Paciano , who acted as his second father. The two brothers rode in a carromata,
          reaching their destination after one and one-half hours’ drive. They proceeded to their aunt’s house, where Jose was to lodge.
          It was almost night when they arrived, and the moon was about to rise.
        </p>
        <p>
          That same night, Jose, with his cousin named Leandro, went sightseeing in the town. Instead of enjoying the sights,
          Jose became depressed because of homesickness. "In the moonlight," he recounted, "I remembered my home town, my idolized mother,
          and my solicitous sisters. Ah, how sweet to me was Calamba, my own town, in spite of the fact that was not as wealthy as Biñan."
        </p>
