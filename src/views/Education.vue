        <h6>
          The Hero’s First Teacher
        </h6>
        <p>
          The first teacher of Rizal was his mother, who was a remarkable woman of good character and fine culture. On her lap, he learned
          at the age of three the alphabet and the prayers. "My mother," wrote Rizal in his student memoirs, "taught me how to read and to say
          haltingly the humble prayers which I raised fervently to God."
        </p>
        <p>
          As tutor, Doña Teodora was patient, conscientious, and understanding. It was she who first discovered that her son had a talent for poetry.
          Accordingly, she encouraged him to write poems. To lighten the monotony of memorizing the ABC’s and to stimulate her son’s imagination,
          she related many stories.
        </p>
        <p>
          As Jose grew older, his parents employed private tutors to give him lessons at home. The first was Maestro Celestino and the second,
          Maestro Lucas Padua. Later, an old man named Leon Monroy, a former classmate of Rizal’s father, became the boy’s tutor. This old teacher
          lived at the Rizal home and instructed Jose in Spanish and Latin. Unfortunately, he did not lived long. He died five months later.
        </p>
        <p>
          After a Monroy’s death, the hero’s parents decided to send their gifted son to a private school in Biñan.
